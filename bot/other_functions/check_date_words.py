def check_year(years):
    if years == 1:
        years_message = "year"
    else:
        years_message = "years"

    return years_message


def check_month(months):
    if months == 1:
        months_message = "month"
    else:
        months_message = "months"

    return months_message


def check_day(days):
    if days == 1:
        days_message = "day"
    else:
        days_message = "days"

    return days_message
